
public class TestaRepeticao {

	public static void main(String[] args) {
		
		// While
		int contador = 0;
		
		while(contador <= 10) {
			System.out.println("Valor do contador no while: " + contador);
			contador++;
		}
		
		// For
		for(int conttador = 0; contador <= 10; contador++) {
			System.out.println(contador);
		}
	}

}
